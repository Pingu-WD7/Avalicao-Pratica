document.addEventListener('DOMContentLoaded', function() {
    const agentesLink = document.querySelector('a[href="/html/cubo.html"]');
    if (agentesLink) {
        agentesLink.addEventListener('click', function() {
            alert('Você está sendo direcionado para a seção do cubo magico!');
        });
    }
});

